CREATE TABLE Addresses (
	AddressID INT(10) NOT NULL AUTO_INCREMENT,
	Street TEXT NOT NULL,
	City TEXT NOT NULL,
	StateCode CHAR(2) NOT NULL,
	ZipCode CHAR(5) NOT NULL,
	PRIMARY KEY (AddressID),
	UNIQUE (Street, City, StateCode, ZipCode),
	CHECK (StateCode REGEXP '[A-Z]{2}'),
	CHECK (ZipCode REGEXP '[0-9]{5}')
)
COLLATE='utf8_general_ci'
ENGINE=InnoDB;

CREATE TABLE Pharmacies (
	PharmacyID INT(10) NOT NULL AUTO_INCREMENT,
	Name TEXT NOT NULL,
	EmailAddress TEXT NOT NULL,
	PhoneNumber CHAR(12) NOT NULL,
	AddressID INT(10) NOT NULL,
	PRIMARY KEY (PharmacyID),
	FOREIGN KEY (AddressID) REFERENCES Addresses(AddressID),
	UNIQUE (Name),
	UNIQUE (EmailAddress),
	UNIQUE (PhoneNumber),
	UNIQUE (AddressID),
	CHECK (EmailAddress REGEXP '[A-Za-z0-9.]*@[A-Za-z0-9.]*\.[A-Za-z0-9]*'),
	CHECK (PhoneNumber REGEXP '[0-9]{3}-[0-9]{3}-[0-9]{4}')
)
COLLATE='utf8_general_ci'
ENGINE=InnoDB;

